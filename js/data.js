// Get trend icon with position
function getTrendIcon(trend, position = 0) {
    switch (trend) {
        case 'up': 
            if (position > 0) {
                return `<div class="trend-with-number">
                    <i class="fas fa-arrow-up trend-up"></i>
                    <span class="trend-number">+${position}</span>
                </div>`;
            } else {
                return '<i class="fas fa-arrow-up trend-up"></i>';
            }
        case 'down': 
            if (position > 0) {
                return `<div class="trend-with-number">
                    <i class="fas fa-arrow-down trend-down"></i>
                    <span class="trend-number">${position}</span>
                </div>`;
            } else {
                return '<i class="fas fa-arrow-down trend-down"></i>';
            }
        case 'new': 
            return '<span class="trend-new">NEW</span>';
        default: 
            return '<i class="fas fa-minus trend-same"></i>';
    }
}

// Get trend text for tooltip
function getTrendText(trend, position = 0) {
    switch (trend) {
        case 'up': 
            return position > 0 ? `เลื่อนขึ้น ${position} อันดับจากอันดับก่อนหน้า` : 'เลื่อนขึ้นจากอันดับก่อนหน้า';
        case 'down': 
            return position > 0 ? `เลื่อนลง ${position} อันดับจากอันดับก่อนหน้า` : 'เลื่อนลงจากอันดับก่อนหน้า';
        case 'new': 
            return 'เพลงใหม่เข้าชาร์ต';
        default: 
            return 'ไม่เปลี่ยนแปลงจากอันดับก่อนหน้า';
    }
}

// Get stats tooltip text
function getStatTooltip(stat) {
    switch (stat) {
        case 'peak': return 'อันดับสูงสุดที่เคยได้';
        case 'streak': return 'จำนวนสัปดาห์ที่ติดชาร์ต';
        case 'streams': return 'จำนวนการฟังทั้งหมด';
        case 'weeksOnChart': return 'จำนวนสัปดาห์ที่ติดชาร์ต';
        default: return '';
    }
}

